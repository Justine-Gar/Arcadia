              <strong>Éducation environnementale</strong> : Organisation d'ateliers et de sessions éducatives pour sensibiliser les visiteurs à la conservation et à la protection de la nature. <br>
              <strong>Partenariats</strong> : Collaboration avec des organisations locales et internationales pour des projets de conservation et de recherche. <br>
              <strong>Gestion des déchets</strong> : Mise en place de systèmes de tri et de compostage pour minimiser notre impact environnemental.
            </p>
          </div>
        </details>

