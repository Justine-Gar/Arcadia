      <h2>Qui somme nous ?</h2>

      <div class="half">

        <details class="tab">
          <summary for="tab-eleven">Qui a crée le Zoo Arcadia ?</summary>
          <div class="tab-content">
            <p>Le Zoo Arcadia a été fondé en 1960 près de la forêt de Brocéliande en Bretagne par José, un passionné de la faune et de la flore, avec l'objectif de créer un espace où les visiteurs peuvent découvrir et apprécier la diversité du monde animal tout en promouvant la conservation et l'éducation environnementale.</p>
          </div>
        </details>

        <details class="tab">
          <summary for="tab-twelve">Quelle est la superficie du zoo ?</summary>
          <div class="tab-content">
            <p>Le Zoo Arcadia s'étend sur une superficie de 35 hectares, offrant ainsi un vaste espace pour les animaux et des zones de promenade agréables pour les visiteurs.</p>
          </div>
        </details>

        <details class="tab">
          <summary for="tab-threenteen">Comment sengage Arcadia sur le developpement durable ?</summary>
          <div class="tab-content">
            <p>
              <strong>Énergies renouvelables</strong> : Le zoo utilise des sources d'énergie renouvelable comme les panneaux solaires et les éoliennes pour alimenter nos installations, réduisant ainsi notre dépendance aux énergies fossiles. <br>
              <strong>Gestion de l'eau</strong> : Nous avons des systèmes de collecte et de réutilisation des eaux de pluie pour l'irrigation et les besoins des animaux, ainsi que des dispositifs de réduction de la consommation d'eau dans tout le parc. <br>
              <strong>Réduction des déchets</strong> : Nous avons mis en place des programmes de tri sélectif et de compostage pour minimiser les déchets envoyés en décharge. Nous encourageons également les visiteurs à participer à ces initiatives en fournissant des points de collecte dans tout le parc. <br>
              <strong>Conservation des habitats naturels</strong> : Le Zoo Arcadia travaille à la préservation et à la restauration des habitats naturels à l'intérieur et à l'extérieur du zoo, favorisant la biodiversité locale. <br>
              <strong>Matériaux durables</strong> : Lors de la construction et de l'entretien des installations, nous utilisons des matériaux durables et écologiques pour réduire notre impact environnemental. <br>
              <strong>Sensibilisation et éducation</strong> : Nous organisons des ateliers et des campagnes de sensibilisation pour éduquer le public sur l'importance de la protection de l'environnement et des pratiques durables.
            </p>
          </div>
        </details>

        <details class="tab">
          <summary for="tab-fourteen">Quelles sont las actions concrètes du Zoo Arcadia ?</summary>
          <div class="tab-content">
