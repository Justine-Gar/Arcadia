        <details class="tab">
          <summary for="tab-seventeen">Quelle sont les condition d'acceuil d'un d'un stagière animalier ?</summary>
          <div class="tab-content">
            <p>
              Le Zoo Arcadia accueille des stagiaires animaliers sous certaines conditions : <br>
              Âge minimum : Les candidats doivent être âgés d'au moins 18 ans. <br>
              Formation : Les stagiaires doivent être inscrits dans une formation en lien avec les soins aux animaux, la zoologie, la biologie ou un domaine connexe. <br>
              Durée du stage : La durée minimale du stage est généralement de 3 mois pour garantir une expérience significative. <br>
              Assurance : Les stagiaires doivent fournir une attestation d'assurance responsabilité civile. <br>
              Motivation et engagement : Les candidats doivent démontrer une forte motivation et un intérêt pour le travail avec les animaux.
            </p>
          </div>
        </details>

        <details class="tab">
          <summary for="tab-heigteen">Comment fait-on pour devenir soigneur animalier ?</summary>
          <div class="tab-content">
            <p>
              Devenir soigneur animalier au Zoo Arcadia nécessite généralement les étapes suivantes : <br>
              Formation académique : Obtenez un diplôme en zoologie, biologie animale, gestion de la faune ou un domaine connexe. Des formations spécialisées pour devenir soigneur animalier sont également disponibles dans certaines écoles. <br>
              Stages et expérience : Acquérez de l'expérience pratique grâce à des stages dans des zoos, des refuges pour animaux ou des réserves naturelles. <br>
              Compétences : Développez des compétences en soins aux animaux, en observation du comportement animal, en alimentation et en gestion des habitats. <br>
              Certification : Certaines institutions proposent des certifications pour les soigneurs animaliers, ce qui peut être un atout supplémentaire. <br>
              Candidature : Lorsque vous avez acquis l'expérience et les qualifications nécessaires, postulez aux postes de soigneur animalier disponibles en envoyant votre CV et une lettre de motivation. <br>
              Retrouvez plus de détails sur le métier de soigneur animalier et les formations à suivre sur le site internet: <br>
